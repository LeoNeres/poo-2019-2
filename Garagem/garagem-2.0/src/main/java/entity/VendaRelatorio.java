package entity;

public class VendaRelatorio {
	
	private String vendedor;
	private float total_venda;
	private float total_comissao;
	
	public String getVendedor() {
		return vendedor;
	}
	public void setVendedor(String vendedor) {
		this.vendedor = vendedor;
	}
	public float getTotal_venda() {
		return total_venda;
	}
	public void setTotal_venda(float total_venda) {
		this.total_venda = total_venda;
	}
	public float getTotal_comissao() {
		return total_comissao;
	}
	public void setTotal_comissao(float total_comissao) {
		this.total_comissao = total_comissao;
	}
	
}
